
CREATE TABLE RESIDENTS(
Resident_Identifier NUMBER(10),
PRIMARY KEY(Resident_Identifier),
SSN NUMBER(9) UNIQUE NOT NULL,
First_Name VARCHAR(500) NOT NULL,
Last_Name VARCHAR (500) NOT NULL,
Street_Address VARCHAR(1000) NOT NULL,
Birthdate DATE NOT NULL);

CREATE TABLE TESTING_CENTERS (
Testing_Center_Identifier NUMBER(10),
Resident_Identifier NUMBER(10) NOT NULL,
FOREIGN KEY(Resident_Identifier) REFERENCES
RESIDENTS(Resident_Identifier),
PRIMARY KEY(Testing_Center_Identifier,
Resident_Identifier),
Center_Name VARCHAR(200) NOT NULL,
Phone NUMBER(11) NOT NULL,
Street_Address VARCHAR(500) NOT NULL);


CREATE TABLE MULTIPLE_PHONES(
Multiple_Phones NUMBER(11) NOT NULL,
Resident_Identifier NUMBER(10) NOT NULL,
FOREIGN KEY (Resident_Identifier) REFERENCES
RESIDENTS (Resident_Identifier),
PRIMARY KEY(Multiple_Phones, Resident_Identifier));

CREATE TABLE ZIP_CODES(
Zip_Code_Identifier NUMBER(10),
PRIMARY KEY(Zip_Code_Identifier),
Zip_Code NUMBER(5) UNIQUE,
Resident_Identifier NUMBER(10),
Testing_Center_Identifier NUMBER(10),
CONSTRAINT FK_RESIDENT FOREIGN KEY (Resident_Identifier,Testing_Center_Identifier)
REFERENCES  TESTING_CENTERS(Resident_Identifier,Testing_Center_Identifier));


CREATE TABLE CITIES(
City_Identifier NUMBER(10),
PRIMARY KEY(City_Identifier),
City_Name VARCHAR(250) NOT NULL,
Population NUMBER(10) NOT NULL,
Zip_Code_Identifier NUMBER(10) NOT NULL,
FOREIGN KEY (Zip_Code_Identifier) REFERENCES
ZIP_CODES(Zip_Code_Identifier));

CREATE TABLE COUNTIES(
County_Identifier NUMBER(10),
PRIMARY KEY(County_Identifier),
County_Name VARCHAR(500) UNIQUE NOT NULL,
Population NUMBER(10) NOT NULL,
City_Identifier NUMBER(10) NOT NULL,
FOREIGN KEY(City_Identifier) REFERENCES
CITIES(City_Identifier));

